import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const mdPath = path.join(__dirname, '../results/multi-tokenizer.md');
const content = fs.readFileSync(mdPath, 'utf8');

const lines = content.split(/\r?\n/);

let currentDataset = '';
let isSynthetic = true;

const datasets = [];
let currentDatasetObj = null;

// Translation map
const translationMap = {
  'Uniform employee records': 'Registros de empleados (uniforme)',
  'E-commerce orders with nested structures': 'Órdenes e-commerce (anidado)',
  'Time-series analytics data': 'Analítica temporal (series temp.)',
  'Top 100 GitHub repositories': 'Repositorios GitHub (sintético)',
  'Semi-uniform event logs': 'Logs de eventos (semi-uniforme)',
  'Deeply nested configuration': 'Config. profundamente anidada',
  // Real world
  'canada.json': 'canada.json (Geometría)',
  'citm_catalog.json': 'citm\\_catalog.json (Catálogo)',
  'currencies.json': 'currencies.json (Monedas)',
  'european-countries.json': 'european-countries.json (Países)',
  'fakestore_business.json': 'fakestore\\_business.json',
  'fakestore_struct.json': 'fakestore\\_struct.json',
  'fakestore_struct_1k.json': 'fakestore\\_struct\\_1k.json',
  'file-extensions.json': 'file-extensions.json',
  'github-repos.json': 'github-repos.json (Repositorios)',
  'github.json': 'github.json (API Github)',
  'hikes_20.json': 'hikes\\_20.json',
  'http-status-codes.json': 'http-status-codes.json',
  'keyboard-shortcuts.json': 'keyboard-shortcuts.json',
  'lorem-ipsum.json': 'lorem-ipsum.json',
  'mountains.json': 'mountains.json',
  'programming-languages.json': 'programming-languages.json',
  'sports-teams-nfl.json': 'sports-teams-nfl.json',
  'test-edge-cases.json': 'test-edge-cases.json (Bordes)',
  'twitter.json': 'twitter.json (Tweets)',
  'units-of-measurement.json': 'units-of-measurement.json',
  'us-capitals.json': 'us-capitals.json',
  'us-states-with-detail.json': 'us-states-with-detail.json',
  'world-cities.json': 'world-cities.json'
};

for (const line of lines) {
  if (line.startsWith('## ')) {
    currentDatasetObj = null;
    continue;
  }
  if (line.startsWith('### Synthetic datasets')) {
    isSynthetic = true;
    continue;
  }
  if (line.startsWith('### Real-world JSON files')) {
    isSynthetic = false;
    continue;
  }
  
  if (line.startsWith('### ')) {
    const name = line.replace('### ', '').trim();
    currentDataset = name;
    currentDatasetObj = {
      rawName: name,
      name: translationMap[name] || name,
      isSynthetic,
      rows: {}
    };
    datasets.push(currentDatasetObj);
    continue;
  }
  
  if (line.startsWith('|') && currentDatasetObj) {
    const parts = line.split('|').map(p => p.trim());
    if (parts.length < 9) continue;
    const format = parts[1];
    if (format === 'LOON (llm)' || format === 'LOON (full)' || format === 'TOON') {
      // Tokenizer values are in parts[2] to parts[8]
      const values = [];
      for (let i = 2; i <= 8; i++) {
        const cell = parts[i];
        const match = cell.match(/\(([-+−\d.]+%?)\)/);
        if (match) {
          let pct = match[1];
          pct = pct.replace('−', '-');
          values.push(pct);
        } else if (cell.includes('n/a')) {
          values.push('n/a');
        } else {
          values.push('0.0%');
        }
      }
      currentDatasetObj.rows[format] = values;
    }
  }
}

let out = '% LaTeX Tables generated by script\n';

const formatVal = (v) => {
  if (v === 'n/a') return 'n/a';
  const escaped = v.replace('%', '\\%');
  return `$${escaped}$`;
};

function printTable(filterFn, title, label) {
  out += `\\begin{table*}[t]\n`;
  out += `  \\centering\n`;
  out += `  \\caption{${title}}\n`;
  out += `  \\label{tab:${label}}\n`;
  out += `  \\scriptsize\n`;
  out += `  \\begin{tabular}{@{}llccccccc@{}}\n`;
  out += `    \\toprule\n`;
  out += `    \\textbf{Dataset} & \\textbf{Modo} & \\textbf{GPT-4o} & \\textbf{GPT-4} & \\textbf{Claude} & \\textbf{Gemini} & \\textbf{Llama 3.2} & \\textbf{Qwen3} & \\textbf{Gemma 3} \\\\\n`;
  out += `     & & \\scriptsize (o200k) & \\scriptsize (cl100k) & \\scriptsize (lenml) & \\scriptsize (lenml) & \\scriptsize (local) & \\scriptsize (local) & \\scriptsize (local) \\\\\n`;
  out += `    \\midrule\n`;
  
  const filtered = datasets.filter(filterFn);
  for (let idx = 0; idx < filtered.length; idx++) {
    const ds = filtered[idx];
    const name = ds.name;
    const toonRow = ds.rows['TOON'] || [];
    const llmRow = ds.rows['LOON (llm)'] || [];
    const fullRow = ds.rows['LOON (full)'] || [];
    
    out += `    \\multirow{3}{*}{${name}} & TOON & ${toonRow.map(formatVal).join(' & ')} \\\\\n`;
    out += `     & \\texttt{llm} & ${llmRow.map(formatVal).join(' & ')} \\\\\n`;
    out += `     & \\texttt{full} & ${fullRow.map(formatVal).join(' & ')} \\\\\n`;
    if (idx < filtered.length - 1) {
      out += `    \\cline{2-9}\n`;
    }
  }
  
  out += `    \\bottomrule\n`;
  out += `  \\end{tabular}\n`;
  out += `\\end{table*}\n`;
}

out += '\n% --- SYNTHETIC DATASETS ---\n';
printTable(ds => ds.isSynthetic, 'Eficiencia en tokens de LOON en conjuntos de datos sintéticos (Deltas vs.\\ JSON-compacto)', 'tokens-synth');

out += '\n% --- REAL-WORLD DATASETS ---\n';
printTable(ds => !ds.isSynthetic, 'Eficiencia en tokens de LOON en conjuntos de datos reales (Deltas vs.\\ JSON-compacto)', 'tokens-real');

fs.writeFileSync(path.join(__dirname, '../latex_tables.tex'), out, 'utf8');
console.log('Successfully wrote to latex_tables.tex');
